function gallery(slug: string): string[] {
  return [1, 2, 3, 4].map((i) => `/images/products/${slug}-gallery-${i}.jpg?v=ai1`);
}

function p(
  partial: Omit<Product, "shippingSummary" | "returnsSummary" | "gallery" | "image"> & {
    imageSlug: string;
    shippingSummary?: string;
    returnsSummary?: string;
  },
): Product {
  const { imageSlug, shippingSummary, returnsSummary, ...rest } = partial;
  return {
    ...rest,
    image: `/images/products/${imageSlug}.jpg?v=ai1`,
    gallery: gallery(imageSlug),
    shippingSummary: shippingSummary ?? ship,
    returnsSummary: returnsSummary ?? returnsApparel,
  };
}

